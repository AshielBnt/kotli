fun main() {
    // Definir tres variables inmutables para los pesos de las personas
    val pesoPersona1: Float = 70.5f
    val pesoPersona2: Float = 65.2f
    val pesoPersona3: Float = 80.8f

    // Calcular el promedio de los pesos
    val promedioPesos = (pesoPersona1 + pesoPersona2 + pesoPersona3) / 3

    // Mostrar el promedio de los pesos
    println("El promedio de los pesos de las personas es: $promedioPesos")
}